def main(x: list):
    data = (
        {'E', 1962, 1958, 1976},
        {'E', 1962, 1958, 1959},
        {'E', 1962, 1958, 1983},

        {'E', 1962, 1986},

        {'E', 1966},

        {'E', 1974, 1976, 1991},
        {'E', 1974, 1976, 2009},
        {'E', 1974, 1976, 2001},

        {'E', 1974, 1959, 1991},
        {'E', 1974, 1959, 2009},
        {'E', 1974, 1959, 2001},

        {'E', 1974, 1983},

        {'MASK'},

        {1986, 'LASSO', 2019},

        {1986, 'CSV'},
        {1986, 'TEA'},

        {1999, 1977},
        {1999, 1994},

        {1999, 2005, 'ASP', 1997},
        {1999, 2005, 'ASP', 2019},

        {1999, 2005, 'EAGLE'},

        {1991}
    )
    x = set(x)
    for key, item in enumerate(data):
        if item.issubset(x):
            return key
    return -1


print(main([1986, 'E', 1966, 1959, 2001]))  # 4
print(main([1986, 'E', 1962, 1983, 2001]))  # 3
print(main([1986, 'MASK', 1966, 1976, 1991]))  # 12
print(main([1958, 'E', 1962, 1976, 2009]))  # 0
print(main([1958, 'E', 1962, 1959, 1991]))  # 1
